import mongoose from "mongoose";

const todaoSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    unique: true,
  },
});

export const Todo = mongoose.model("Todo", todaoSchema);
